n_in  = 2;
n_middle1 = 6;
n_middle2 = 9;
n_out = 4;

W1 = 2 * rand(n_middle1, n_in + 1) - 1;
W2 = 2 * rand(n_middle2, n_middle1 + 1) - 1;
W3 = 2 * rand(n_out , n_middle2 + 1) - 1;

%W1 = [-5.610178594751309,0.220921071101528,0.875040696968087;0.209195526880620,3.643609673741753,-0.585734291327199];
%W2 = [-1.527146392319680,1.385365970700792,3.187447013420893;0.979321348454371,2.227885538080948,1.625213891378540];
%W3 = [0.700399008526771,0.393397358100480,-0.343210361637855;0.349992028881816,0.202788257962965,-0.168859007427732;-3.28307320379492,0.00364399663019973,3.27090765677685;1.05956677214521,-1.20175399157776,0.139514108601054];

n=100;
Mx=10;
E=Mx*rand([2,n])-Mx/2*ones(2,n); % Mittelwert Null
k1=E(1,:)>=0;
k2=E(2,:)>=0;
A=[and(k1,k2); ... % 1 Quadrant
and(not(k1),k2); ... % 2. Quadrant
and(not(k1),not(k2)); ... % 3. Quadrant
and(k1,not(k2))]; % 4. Quadrant

plot(E(1,A(1,:)),E(2,A(1,:)),'ok'); hold on
plot(E(1,A(2,:)),E(2,A(2,:)),'*k');
plot(E(1,A(3,:)),E(2,A(3,:)),'sk');
plot(E(1,A(4,:)),E(2,A(4,:)),'pk');

[W1,W2,W3] = train2(W1,W2,W3,E,A);

while true
    [ix,iy] = ginput(1);
    
    round(values2(W1,W2,W3,[ix;iy]))
end

